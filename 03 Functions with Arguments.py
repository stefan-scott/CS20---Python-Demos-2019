def multiplier(num1, num2):
    # a function that multiplies two inputed values
    # and prints the result to the console.
    result = num1 * num2
    print(result)

#Start by asking the user for two input values
input1 = float(input("Please Enter a Number: "))
input2 = float(input("Please Enter a Number: "))

multiplier(13, 23)
multiplier(input1, input2)
multiplier(5, "Hey!")



