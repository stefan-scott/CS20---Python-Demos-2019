# Calculates the area of a given rectangle

#Start by reading input from the user
width = input("What is the rectangle width? ")
height = float(input("What is the rectangle height? "))

#Convert both variables to floats
width = float(width)

#Calculate the Area:
area = height * width

#Print out the Result:
print("The area of that rectangle is:", area)

